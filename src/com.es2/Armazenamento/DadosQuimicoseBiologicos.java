package com.es2.Armazenamento;

public class DadosQuimicoseBiologicos {
    Double colesterol;
    Double glicemia;
    Double ureia;
    Double creatinina;
    Double proteinaCReactiva;

    public DadosQuimicoseBiologicos() {
    }

    public Double getColesterol() {
        return colesterol;
    }

    public void setColesterol(Double colesterol) {
        this.colesterol = colesterol;
    }

    public Double getGlicemia() {
        return glicemia;
    }

    public void setGlicemia(Double glicemia) {
        this.glicemia = glicemia;
    }

    public Double getUreia() {
        return ureia;
    }

    public void setUreia(Double ureia) {
        this.ureia = ureia;
    }

    public Double getCreatinina() {
        return creatinina;
    }

    public void setCreatinina(Double creatinina) {
        this.creatinina = creatinina;
    }

    public Double getProteinaCReactiva() {
        return proteinaCReactiva;
    }

    public void setProteinaCReactiva(Double proteinaCReactiva) {
        this.proteinaCReactiva = proteinaCReactiva;
    }
}
